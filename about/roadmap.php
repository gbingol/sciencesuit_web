<h1>Road Map</h1>

<p>
    Since its <a href="../downloads/index.php">first release</a> in <em>Dec, 25 2016</em>, 
    ScienceSuit has come a very long way and has improved considerably in many innumerable ways. 
    
    Thanks to its modular and extensible design, the journey  of R&D of ScienceSuit has no boundaries 
    and therefore a roadmap, even a tentative one, is essential.
</p>



<p>The following work will be the focus of the next few <em>major</em> releases:</p>
<ul class="spaced">
    <li>
        The next few releases will focus on more <a href="../manual/plotter/index.php">chart</a> 
        types (histogram, line, bar and pie). 
    </li>

    <li>
        As of (v1.9.x) due to its popularity and demand more <a href="../manual/apps/index.php">apps</a>; 
        on process engineering and on data analytics. 
    </li>

    <li>
        As of (v1.9.x) a Camera Toolbox for acquiring live images from cameras.
    </li>

</ul>
    
    
