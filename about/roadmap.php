<h2>Want new features?</h2>
<p>
    <em>ScienceSuit</em> is comprised of several components and it is appreciated 
    if you would like to pitch in. How? Basically, in two ways:
</p>

<p>
    <b>1)</b> Submitting a new feature: Please check  <a href="https://github.com/gbingol" target="_blank">GitHub</a>
</p>



<p>
    <b>2)</b> <em>Requesting</em> a new feature: 
</p>
<ul class="spaced">
    <li>
        There is a feature missing in ScienceSuit and its inclusion 
        will be beneficial. If your request is aligned with the road map
        it might be included in the next releases.</li>

    <li>
        For purpose-specific features, 
        please contact <a href="mailto:gbingol@sciencesuit.org">gbingol@sciencesuit.org</a> 
    </li>
</ul>


<p>&nbsp;</p>

