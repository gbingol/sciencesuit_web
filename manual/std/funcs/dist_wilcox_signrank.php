<h2 id="qsignrank">qsignrank</h2>
<p>Computes the quantile. </p>

<p class="funcsignature">
    
    qsignrank{p=, n=} &rarr; Vector / number<br>
    
    <br>
    
    qsignrank(p=, n=) &rarr; Vector / number
   
</p>

<p>
    <em>Note: </em>The function rounds up the result.
</p>

<p class="CodeCommand">
    &gt;&gt;std.<mark>p</mark>signrank{q=2, n=4} <br />
    0.1875 <br />

    <br />
    <br />

    &gt;&gt;std.qsignrank{p=0.18, n=4} <br />
    2	<br />

    <br />

    &gt;&gt;std.qsignrank{p=0.1876, n=4} <br />
