<h2 id="dsignrank">dsignrank</h2>
<p>Computes the probability. </p>

<p class="funcsignature">
    
    dsignrank{x=, n=} &rarr; Vector / number<br>
    
    <br>
    
    dsignrank(x=, n=) &rarr; Vector / number
   
</p>


<p>
    <em>Note:</em> If the argument x is greater than the number of coefficients 
    generated by argument n, or if x&lt;0, the function returns 0.0. 
</p>

<p>&nbsp;</p>

<p class="CodeCommand">
    &gt;&gt;std.dsignrank{x=2, n=4} <br />
    0.0625	<br />

    <br />

    &gt;&gt;std.dsignrank(2,4) <br />
    0.0625	<br />

    <br />

    &gt;&gt;v=std.tovector{2, 3} <br />
    &gt;&gt;std.dsignrank{x=v, n=4} <br />
    0.0625&nbsp;&nbsp;&nbsp;&nbsp; 0.125&nbsp;&nbsp;&nbsp;&nbsp; COL
</p>


<p>&nbsp;</p>

<div>
    <p>Mathematically:</p>
    
    <img src="../images/dist_wilcoxsignrank_mw_closed.png" alt=""/> <br />
    <img alt="" src="../images/dist_wilcoxsignrank_mw_open.png" />

    <p>
        For x=2, the coefficient of e<sup>2t</sup> is 1. Therefore 1/16=0.0625. <br />
        For x=3, the coefficient of e<sup>3t</sup> is 2. Therefore 2/16=0.125.
    </p>
</div>










