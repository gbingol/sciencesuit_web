<h2 id="psignrank">psignrank</h2>
<p>Computes the cumulative probability. </p>

<p class="funcsignature">
    
    psignrank{q=, n=} &rarr; Vector / number<br>
    
    <br>
    
    psignrank(q=, n=) &rarr; Vector / number
   
</p>

<p>
    Note: If argument <em>q</em> is not an integer number, then it is rounded to the nearest integer number.
</p>



<p class="CodeCommand">
    &gt;&gt;std.psignrank{q=2, n=4} <br />
    0.1875	<br />

    <br />

    &gt;&gt;std.psignrank{q=3, n=4} <br />
    0.3125	<br />

    <br />
    <br />

    &gt;&gt;std.psignrank{q=2.4, n=4} <span class="LuaComment">--rounded to 2</span> <br />
    0.1875	<br />

    <br />

    &gt;&gt;std.psignrank{q=2.6, n=4} <span class="LuaComment">--rounded to 3</span> <br />
    0.3125	
</p>










